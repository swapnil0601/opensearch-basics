  const calculateCountryStats = (matchData) => {
    const countryStats = {};
  
    matchData.forEach((match) => {
      const { team1, team2, winner, team1_runs, team2_runs, team1_wickets, team2_wickets } = match._source;
  
      const teams = [team1, team2];
      teams.forEach((team) => {
        if (!countryStats[team]) {
          countryStats[team] = { totalRuns: 0, totalWickets: 0 };
        }
  
        if (team === winner) {
          countryStats[team].totalRuns += team1 === team ? team1_runs : team2_runs;
          countryStats[team].totalWickets += team1 === team ? team1_wickets : team2_wickets;
        }
      });
    });
  
    return countryStats;
  };
  
